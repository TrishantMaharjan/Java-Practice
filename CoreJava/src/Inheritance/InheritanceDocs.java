package Inheritance;

public class InheritanceDocs {
	/*
	 * ========== Inheritance ===========
	 * # Code re-usability
	 * # Process of accessing properties and methods of parent/super/base class to child/sub/derived class
	 * # Also known as IS-A relationship (Eg. Alto 800 is a vehicle)
	 * # Is a generalization to specialization process
	 * # Can not inherit private properties, and constructor
	 * # Can inherit static variables and methods
	 * # Can inherit only one class at a time ie. only one parent class at a time
	 * 
	 * Syntax:
	 * 	class child_class_name extends parent_class_name{
	 * 		// properties -> parent properties + child properties
	 * 		// methods -> parent methods + child methods
	 * 	}
	 * 
	 * Eg:
	 * 	class A {
	 * 		
	 * 	}
	 * 
	 * 	class B extends A {
	 * 		
	 * 	}
	 */
}
