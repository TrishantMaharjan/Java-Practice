package Collection;

public class CollectionDocs {
	/*
	 * ========== Collection Framework ==========
	 * # used to store and process dynamic multiple data/objects in java
	 * 
	 * 1> List: Dynamic Array
	 * 		a> ArrayList : Uses stack; Slow processing, better to use for data storage
	 * 		b> LinkedList : Fast processing, better to use for data processing
	 * 2> Set: Collection of unique data
	 * 		a> HashSet : Unique data only
	 * 		b> TreeSet : Unique and sorted
	 * 		c> LinkedHashSet : Unique and insertion in order
	 * 3> Map: Data stored in key, value pair
	 * 		a> HashMap : Key Value (<K,V>) pair
	 * 		b> TreeMap : <K,V> and sorted by key
	 * 		c> LinkedHashMap : <K,V> and insertion in order
	 */
}
