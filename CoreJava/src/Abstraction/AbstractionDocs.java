package Abstraction;

public class AbstractionDocs {
	/*
	 * ========== Abstraction ==========
	 * # process of hiding implementation of details in the program is known as abstraction
	 * # to achieve abstraction:
	 * 		1> Abstract class:
	 * 			Class which contains abstract (unimplemented) and non-abstract (implemented) methods is known as abstract class.
	 * 			
	 * 			Abstract method:
	 * 				Method which does not have implementation/body.
	 * 				-> We can't create object of abstract class.
	 * 				-> Every abstract class must have atleast one child class and child class must override abstract methods.
	 *  			syntax:
	 *  				abstract class class_name {
	 *  					// abstract methods
	 *  						abstract method_type method_name();
	 *  					// non-abstract methods
	 *  				}			
	 *  
	 * 		2> Interface:
	 * 			> Same as class which contains public abstract methods and public static final variables only.
	 * 			> We can't create object of interface
	 * 			> Every interface must have implementation class and must override abstract methods
	 * 			> It's a 100% abstraction
	 * 			syntax:
	 * 				interface interface_name {
	 * 					// public abstract methods
	 * 					// public static final variables
	 * 				}
	 */
}
